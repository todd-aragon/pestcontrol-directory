def content_for(key):
    """Fetch cached Gemini copy, or None. {intro, faq:[{q,a}]}."""
    row = get_db().execute(
        "SELECT intro, faq FROM content WHERE key = ?", (key,)
    ).fetchone()
    if not row:
        return None
    return {"intro": row["intro"],
            "faq": json.loads(row["faq"]) if row["faq"] else []}


